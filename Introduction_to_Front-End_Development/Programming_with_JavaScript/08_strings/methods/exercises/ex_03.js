/*
  CHANGE CASE OF A STRING
  -----------------------

  String.toUpperCase() - changes all the letters of a string to uppercase.

  String.toLowerCase() - changes all the letters of a string to lowercase.

  NOTE - All other characters remain intact.

*/

// String.toUpperCase()
const str1 = "This is Indian Millitary Academy, one of the finest military academies in the world.";

const str1ToUppercase = str1.toUpperCase();
console.log("The uppercase version of the above string :", str1ToUppercase);
console.log();

console.log("The lowercase version of the above string :", str1ToUppercase.toLowerCase());
console.log();

const email = "SAHA.NILADRI26@EXAMPLE.COM";
console.log("The correct email ID will be:", email.toLowerCase());

